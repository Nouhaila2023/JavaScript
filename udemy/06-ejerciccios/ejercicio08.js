/*Ejercicio 8
Tenemos una heladeria y cada bola de helado vale 1.8€
¿Cuano cuestan los cucirichos de 1,2, y 3 bolas
Ten en cuenta que el barquillo cuenta cincuenta centimos de euros*/

let precioBola = 1.8;
let precioCucurucho = 0.5;

let precio1Bola = (precioBola * 1) + precioCucurucho;
let precio2Bolas = (precioBola * 2) + precioCucurucho;
let precio3Bolas = (precioBola * 3) + precioCucurucho;

console.log(
    `El precio de un cucurucho de 1 bola es ${precio1Bola}€
     El precio de un cucurucho de 2 bolas es ${precio2Bolas}€
     El precio de un cucurucho de 3 bolas es ${precio3Bolas}€`
);  


